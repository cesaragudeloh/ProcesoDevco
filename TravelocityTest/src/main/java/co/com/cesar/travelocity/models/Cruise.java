package co.com.cesar.travelocity.models;

public class Cruise {

    private String goingTo;
    private String traveles;
    private String departsAsEarlyAs;
    private String departsAslateAs;


    public String getGoingTo() {
        return goingTo;
    }

    public String getTraveles() {
        return traveles;
    }

    public String getDepartsAsEarlyAs() {
        return departsAsEarlyAs;
    }

    public String getDepartsAslateAs() {
        return departsAslateAs;
    }
}
